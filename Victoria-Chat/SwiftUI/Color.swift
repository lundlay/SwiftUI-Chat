import SwiftUI

extension Color {
    
    static var bubbleShadow: Color {
        return Color(UIColor.bubbleShadow)
    }

    static var bubbleBackground: Color {
        return Color(UIColor.bubbleBackground)
    }

    static var viewBackground: Color {
        return Color(UIColor.viewBackground)
    }

}

